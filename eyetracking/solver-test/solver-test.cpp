const size_t sampleCount = (sizeof(data) / sizeof(data[0])) / kCalibrationSampleColumns;


double evaluate(const double* coeffs, double x_, double y_) {
  const double x2 = x_ * x_;
  const double x3 = x2 * x_;

  const double y2 = y_ * y_;
  const double y3 = y2 * y_;

  return
      coeffs[0]
    + (coeffs[1] * x_)
    + (coeffs[2] * x2)
    + (coeffs[3] * x3)
    + (coeffs[4] * y_)
    + (coeffs[5] * x_ * y_)
    + (coeffs[6] * x2 * y_)
    + (coeffs[7] * y2)
    + (coeffs[8] * x_ * y2)
    + (coeffs[9] * y3);
}

struct BicubicResidual {
  BicubicResidual(double x, double y, double f) : x_(x), y_(y), f_(f) {}

  // a + bx + cx^2 + dx^3 + ey + fxy + gx^2y + hy^2 + ixy^2 + jy^3

  template <typename T>
  bool operator()(
    const T* const coeffs,
    T* residual) const {

    const double x2 = x_ * x_;
    const double x3 = x2 * x_;

    const double y2 = y_ * y_;
    const double y3 = y2 * y_;

    T pred =
          coeffs[0]
        + (coeffs[1] * x_)
        + (coeffs[2] * x2)
        + (coeffs[3] * x3)
        + (coeffs[4] * y_)
        + (coeffs[5] * x_ * y_)
        + (coeffs[6] * x2 * y_)
        + (coeffs[7] * y2)
        + (coeffs[8] * x_ * y2)
        + (coeffs[9] * y3);

    residual[0] = pred - f_;

    return true;
  }
 private:
  const double x_;
  const double y_;
  const double f_;
};

int main(int argc, char** argv) {
  double xCoeffs[10];
  double yCoeffs[10];
  memset(xCoeffs, 0, sizeof(xCoeffs));
  memset(yCoeffs, 0, sizeof(yCoeffs));

  ceres::Problem problem;
  for (int sampleIdx = 0; sampleIdx < sampleCount; ++sampleIdx) {
    problem.AddResidualBlock(
        new ceres::AutoDiffCostFunction<BicubicResidual, 1, 10>(
          new BicubicResidual(
            data[(sampleIdx * kCalibrationSampleColumns) + 2],
            data[(sampleIdx * kCalibrationSampleColumns) + 3],
            data[(sampleIdx * kCalibrationSampleColumns) + 0]
          )
        ), new ceres::CauchyLoss(0.5), xCoeffs);

    problem.AddResidualBlock(
        new ceres::AutoDiffCostFunction<BicubicResidual, 1, 10>(
          new BicubicResidual(
            data[(sampleIdx * kCalibrationSampleColumns) + 2],
            data[(sampleIdx * kCalibrationSampleColumns) + 3],
            data[(sampleIdx * kCalibrationSampleColumns) + 1]
          )
        ), new ceres::CauchyLoss(0.5), yCoeffs);
  }

  ceres::Solver::Options options;
  options.max_num_iterations = 1000;
  options.linear_solver_type = ceres::DENSE_QR;
  options.minimizer_progress_to_stdout = true;
  ceres::Solver::Summary summary;
  ceres::Solve(options, &problem, &summary);
  std::cout << summary.BriefReport() << "\n";

  const char* coeffName[] = {
    "",
    "x",
    "x^2",
    "x^3",
    "y",
    "x y",
    "x^2 y",
    "y^2",
    "x y^2",
    "y^3",
  };

  for (size_t i = 0; i < 10; ++i) {
    std::cout << coeffName[i] << " " << xCoeffs[i] << " " << yCoeffs[i] << std::endl;
  }

  std::cout << "const double xCoeffs[] =  {";
  for (size_t i = 0; i < 10; ++i) {
    std::cout << xCoeffs[i] << ", ";
  }
  std::cout << "};" << std::endl;

  std::cout << "const double yCoeffs[] =  {";
  for (size_t i = 0; i < 10; ++i) {
    std::cout << yCoeffs[i] << ", ";
  }
  std::cout << "};" << std::endl;


  for (size_t sampleIdx = 0; sampleIdx < sampleCount; ++sampleIdx) {
    const double* sample = data + (sampleIdx * kCalibrationSampleColumns);
    double xPred = evaluate(xCoeffs, sample[2], sample[3]);
    double yPred = evaluate(yCoeffs, sample[2], sample[3]);
    double xGt = sample[0], yGt = sample[1];

    double dx = xGt - xPred;
    double dy = yGt - yPred;
    double dist = sqrt((dx * dx) + (dy * dy));

    printf("{%.3f, %.3f} -- pred={%.3f, %.3f} gt={%.3f, %.3f} dist=%.3f\n", sample[2], sample[3], xPred, yPred, xGt, yGt, dist);
  }


  return 0;
}

